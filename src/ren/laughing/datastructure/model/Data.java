package ren.laughing.datastructure.model;
/**
 * 此为数据元素默认类型
 * @author Laughing
 * @time 2016年4月5日
 */
public class Data {
	private int id;
	private String name;
	private int length;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	
}
